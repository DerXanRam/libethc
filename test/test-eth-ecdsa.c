void test_eth_ecdsa_sign(void) {
  struct eth_ecdsa_signature sig;
  const uint8_t data32[32] = {0x34, 0x8c, 0xe5, 0x64, 0xd4, 0x27, 0xa3, 0x31,
                           0x1b, 0x65, 0x36, 0xbb, 0xcf, 0xf9, 0x39, 0x0d,
                           0x69, 0x39, 0x5b, 0x06, 0xed, 0x6c, 0x48, 0x69,
                           0x54, 0xe9, 0x71, 0xd9, 0x60, 0xfe, 0x87, 0x09};

  struct eth_ecdsa_signature exp_sig = {
      .r = {0xb7, 0x88, 0x40, 0x0e, 0x00, 0x36, 0x0e, 0xc8, 0x2d, 0xd7, 0x6a,
            0x78, 0x84, 0x45, 0xd8, 0xb5, 0x9b, 0x62, 0xb7, 0xac, 0x94, 0xdc,
            0x9b, 0xbb, 0x7b, 0x28, 0x29, 0x64, 0xf6, 0xc0, 0xf0, 0xe4},

      .s = {0x0a, 0xbb, 0xb2, 0xc2, 0x77, 0xaf, 0xf4, 0x51, 0x6d, 0x6b, 0x59,
            0x44, 0x6c, 0xba, 0xbd, 0x2a, 0x47, 0xf0, 0x6c, 0x68, 0xae, 0x53,
            0x82, 0xfd, 0xf3, 0x24, 0xdf, 0x57, 0x35, 0x87, 0x02, 0xeb},

      .recid = 0x01};

  ok(eth_ecdsa_sign(&sig, data32, data32) == ETHC_SUCCESS);
  cmp_mem(&sig, &exp_sig, sizeof(struct eth_ecdsa_signature));
}
